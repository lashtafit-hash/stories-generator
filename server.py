@app.route("/analyze", methods=["POST"])
def analyze():
    niche = request.json.get("niche", "")
    if not niche:
        return jsonify({"error": "Ниша не указана"}), 400
    user_prompt = f"Проанализируй нишу и определи архетип ЦА: {niche}"
    try:
        result = call_deepseek(ARCHETYPE_SYSTEM, user_prompt, max_tokens=600)
        import json, re
        clean = re.sub(r"```json|```", "", result).strip()
        data = json.loads(clean)
        return jsonify(data)
    except Exception as e:
        return jsonify({"error": str(e), "raw": result if 'result' in dir() else ""}), 500


@app.route("/generate", methods=["POST"])
def generate():
    body = request.json
    niche = body.get("niche", "")
    archetype = body.get("archetype", "")
    archetype_data = body.get("archetype_data", {})
    cat = body.get("cat", "life")
    tone = body.get("tone", "ДРУЖЕЛЮБНЫЙ")
    topic = body.get("topic", "")
    goal = body.get("goal", "")
    count = body.get("count", 5)

    cat_names = {
        "sell": "ПРОДАЮЩИЙ",
        "warmup": "ПРОГРЕВ",
        "life": "ЛАЙФ",
        "expert": "ЭКСПЕРТНЫЙ"
    }

    strategies = ["X-Ray Vision", "The Price of Error", "Internal Dialog", "Myth Buster"]
    chosen_strategy = random.choice(strategies)

    user_prompt = f"""ВХОДНЫЕ ДАННЫЕ:
Ниша/Деятельность: {niche}
Архетип ЦА: {archetype}
Глубинная потребность ЦА: {archetype_data.get('deep_need', '')}
Теневой страх ЦА: {archetype_data.get('shadow_fear', '')}
Тип сторис: {cat_names.get(cat, 'ЛАЙФ')}
Тональность: {tone}
Ситуация/Тема: {topic}
Цель: {goal if goal else 'вовлечённость и доверие'}
Количество сторис: {count}

ОБЯЗАТЕЛЬНО используй стратегию: {chosen_strategy}

Напиши ровно {count} сторис. Только текст для экрана — без описаний визуала и фото. Каждая сторис логически вытекает из предыдущей."""

    try:
        result = call_deepseek(GENERATOR_SYSTEM, user_prompt, max_tokens=1400)
        return jsonify({"result": result, "strategy": chosen_strategy})
    except Exception as e:
        return jsonify({"error": str(e)}), 500


