def choose_scenario(category, topic, goal):
    scenarios = SCENARIOS.get(category, SCENARIOS["life"])
    matchers = SCENARIO_MATCHERS.get(category, {})
    text = f"{topic or ''} {goal or ''}".lower()

    scored = []
    for scenario in scenarios:
        keywords = matchers.get(scenario["name"], [])
        score = sum(1 for keyword in keywords if keyword in text)
        scored.append((score, scenario))

    best_score = max((score for score, _ in scored), default=0)
    top = [scenario for score, scenario in scored if score == best_score]
    return random.choice(top or scenarios)


